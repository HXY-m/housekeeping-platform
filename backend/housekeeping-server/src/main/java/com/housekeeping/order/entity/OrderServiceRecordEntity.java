package com.housekeeping.order.entity;

import com.baomidou.mybatisplus.annotation.IdType;
import com.baomidou.mybatisplus.annotation.TableId;
import com.baomidou.mybatisplus.annotation.TableName;

import java.time.LocalDateTime;

@TableName("booking_order_service_record")
public class OrderServiceRecordEntity {

    @TableId(type = IdType.AUTO)
    private Long id;
    private Long orderId;
    private Long workerId;
    private String stage;
    private String description;
    private LocalDateTime createdAt;

    public OrderServiceRecordEntity() {
    }

    public OrderServiceRecordEntity(Long orderId, Long workerId, String stage, String description, LocalDateTime createdAt) {
        this.orderId = orderId;
        this.workerId = workerId;
        this.stage = stage;
        this.description = description;
        this.createdAt = createdAt;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getOrderId() {
        return orderId;
    }

    public void setOrderId(Long orderId) {
        this.orderId = orderId;
    }

    public Long getWorkerId() {
        return workerId;
    }

    public void setWorkerId(Long workerId) {
        this.workerId = workerId;
    }

    public String getStage() {
        return stage;
    }

    public void setStage(String stage) {
        this.stage = stage;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }
}
